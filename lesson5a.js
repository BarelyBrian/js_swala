// An arrow function is shorter way of defining functions in js drafted from anonymous functions by replacing the keyword function with an arrow
const sayHello = () =>{
    console.log("hello world")
}
// call the function using the variable name
sayHello()

// create an arrow function to log a message saying goodbye
const sayGoodbye = ()=>{
    console.log("Goodbye world  ")
}
sayGoodbye()